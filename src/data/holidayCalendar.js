// Returns today's holiday entry with category data merged in
export function getTodayHoliday(dateOverride) {
  const params = new URLSearchParams(window.location.search);
  const debugDate = params.get('holiday');
  const debugBirthday = params.get('birthday') === 'true';

  let key;
  if (debugDate && /^\d{2}-\d{2}$/.test(debugDate)) {
    key = debugDate;
  } else if (debugBirthday) {
    key = '03-03';
  } else if (dateOverride) {
    const m = String(dateOverride.getMonth() + 1).padStart(2, '0');
    const d = String(dateOverride.getDate()).padStart(2, '0');
    key = `${m}-${d}`;
  } else {
    const now = new Date();
    const m = String(now.getMonth() + 1).padStart(2, '0');
    const d = String(now.getDate()).padStart(2, '0');
    key = `${m}-${d}`;
  }

  let entry = HOLIDAY_CALENDAR[key];

  // Fallback for days not in calendar
  if (!entry) {
    const now = dateOverride || new Date();
    const start = new Date(now.getFullYear(), 0, 0);
    const dayOfYear = Math.floor((now - start) / (1000 * 60 * 60 * 24));
    const fallback = DAILY_FALLBACKS[dayOfYear % DAILY_FALLBACKS.length];
    entry = { tier: 1, ...fallback };
  }

  const category = CATEGORIES[entry.category] || CATEGORIES.tech;

  return {
    ...entry,
    key,
    categoryData: category,
    accentPrimary: category.accentPrimary,
    accentSecondary: category.accentSecondary,
    accentGlow: category.accentGlow,
    particle: category.particle,
    glintMood: category.glintMood,
  };
}
